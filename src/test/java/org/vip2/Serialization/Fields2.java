package org.vip2.Serialization;

public class Fields2 {
	private Project2 project;
	private IssueType2 issuetype;
	private String summary;
	private String description;
	public Project2 getProject() {
		return project;
	}
	public void setProject(Project2 project) {
		this.project = project;
	}
	public IssueType2 getIssuetype() {
		return issuetype;
	}
	public void setIssuetype(IssueType2 issuetype) {
		this.issuetype = issuetype;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

}